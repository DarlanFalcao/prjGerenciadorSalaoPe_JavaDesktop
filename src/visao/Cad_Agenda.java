    private void btPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPagoActionPerformed
            
        String descricao = tfDescricao.getText();
        String data = tfdata.getText();
        String hora = cbHora.getSelectedItem()+":"+cbMinutos.getSelectedItem();
        String valor =  tfValor.getText();
        String pago = null;
        
        String tipo = cbTipoPgto.getSelectedItem().toString();
        String func = cbFuncionario.getSelectedItem().toString();
        funcionario = funcControle.buscaIdFuncionario(func);
        listaserv = servControle.retornaServicoData(dataBanco);
        pago = "SIM";
        int pg = 1;
            try {
                Date datasistema = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                datasistema.getTime();
                String datasis = sdf.format(datasistema);
                String desc = descricao+" Funcionario: "+func;
                entControle.salvarEntrada(desc, datasis, valor, pg , tipo);
            } catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            }   catch (Exception ex) {
               JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        try {
            listaserv = servControle.retornaServicoData(dataBanco);
            int pos = tblAgenda.getSelectedRow();
            Servico servico = listaserv.get(pos);
            servControle.alterarServico(servico.getId(), descricao, valor, pago, data, hora, tipo, funcionario);
            carregaTabela();
            desabilitaCampos();
        } catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            }
    }//GEN-LAST:event_btPagoActionPerformed

