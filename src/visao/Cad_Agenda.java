    public void carregaTabela(){
     DefaultTableModel modelo = (DefaultTableModel) tblAgenda.getModel();
         if(modelo.getRowCount()>0){
            modelo.setRowCount(0);
        }
         listaserv = servControle.retornaServicoData(dataBanco);
         
        for(Servico s:listaserv){
            Object[] o = new Object[6];
            o[0] = s.getData();
            o[1] = s.getHora();
            o[2] = s.getFuncionario().getNome();
            o[3] = s.getDescricao();
            o[4] = s.getValor();
            if(s.getPago()==1){
            o[5] = "SIM";
            }else{
           o[5] = "NÃO";

            }
            modelo.addRow(o);  
        }
}
