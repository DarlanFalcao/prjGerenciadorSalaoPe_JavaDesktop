    private void tblAgendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAgendaMouseClicked
        
        int pos = tblAgenda.getSelectedRow();
        int sel,selMin,selFunc,selTipo;
        sel = selMin = selFunc = selTipo =  0;
        listaserv = servControle.retornaServicoData(dataBanco);
        cbFuncionario.setSelectedItem(listaserv.get(pos));
        tfDescricao.setText(listaserv.get(pos).getDescricao());
        tfValor.setText(listaserv.get(pos).getValor().toString());
        if(listaserv.get(pos).getPago()== 1){
            jrSim.setSelected(true);
            jrNao.setSelected(false);
            btPago.setEnabled(false);
        }else{
            jrNao.setSelected(true);
            jrSim.setSelected(false);
            btPago.setEnabled(true);
        }
        for(int i=0;i<cbHora.getItemCount();i++){
          cbHora.setSelectedIndex(i);
          String hora = cbHora.getSelectedItem().toString();
          if(listaserv.get(pos).getHora().substring(0,2).equals(hora)){
               sel = i;
          }      
          cbHora.setSelectedIndex(sel);
        }
        for(int i=0;i<cbMinutos.getItemCount();i++){
          cbMinutos.setSelectedIndex(i);
          String hora = cbMinutos.getSelectedItem().toString();
          if(listaserv.get(pos).getHora().substring(3,5).equals(hora)){
               selMin = i;
          }      
            cbMinutos.setSelectedIndex(selMin);
        }
        for(int i = 0;i<cbFuncionario.getItemCount();i++){
            cbFuncionario.setSelectedIndex(i);
            String func = cbFuncionario.getSelectedItem().toString();
            if(listaserv.get(pos).getFuncionario().getNome().equals(func)){
                selFunc = i;
            }else{
                selFunc = 0;
            }
            cbFuncionario.setSelectedIndex(selFunc);
        }
        for(int i = 0;i<cbTipoPgto.getItemCount();i++){
            listaserv = servControle.retornaServicoData(dataBanco);
            cbTipoPgto.setSelectedIndex(i);
            String tipo = cbTipoPgto.getSelectedItem().toString();
            if(listaserv.get(pos).getTipo_pgto().equals(tipo)){
                selTipo = i;
            }
            cbTipoPgto.setSelectedIndex(selTipo);
        }
        SimpleDateFormat sp1 = new SimpleDateFormat("dd/MM/yyyy");
        String data = sp1.format(jcCalendario.getDate());
        tfdata.setText(data);
        
    }//GEN-LAST:event_tblAgendaMouseClicked

