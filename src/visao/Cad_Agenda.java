    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        if(!this.altera){
        String descricao = tfDescricao.getText();
        String data = tfdata.getText();
        String hora = cbHora.getSelectedItem()+":"+cbMinutos.getSelectedItem();
        String valor =  tfValor.getText();
        String pago = null;
        String tipo = cbTipoPgto.getSelectedItem().toString();
        String func = cbFuncionario.getSelectedItem().toString();
        funcionario = funcControle.buscaIdFuncionario(func);
        listaserv = servControle.retornaServicoData(dataBanco);
        if(jrSim.isSelected()){
        pago = "SIM";
        int pg = 1;
            try {
                servControle.salvarServico(descricao, valor, pago, data, hora, tipo, funcionario);
                String desc = descricao+" Funcionario: "+func;
                entControle.salvarEntrada(desc, data, valor, pg , tipo);
                carregaTabela();
                desabilitaCampos();
            } catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }   catch (Exception ex) {
               JOptionPane.showMessageDialog(null, ex.getMessage());
            }
            
        }else{
        pago = "NÃO";
        
        try {
            servControle.salvarServico(descricao, valor, pago, data, hora,tipo, funcionario);
              carregaTabela();
              desabilitaCampos();
               
        } catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
        this.altera = false;    
        }    
        }else{
        
        String descricao = tfDescricao.getText();
        String data = tfdata.getText();
        String hora = cbHora.getSelectedItem()+":"+cbMinutos.getSelectedItem();
        String valor =  tfValor.getText();
        String pago = null;
        String tipo = cbTipoPgto.getSelectedItem().toString();
        String func = cbFuncionario.getSelectedItem().toString();
        funcionario = funcControle.buscaIdFuncionario(func);
        listaserv = servControle.retornaServicoData(dataBanco);
        int pos = tblAgenda.getSelectedRow();
        listaserv = servControle.retornaServicoData(dataBanco);
        Servico servico = listaserv.get(pos);
        
        if(jrSim.isSelected()){
        pago = "SIM";
        
        }else{
        
            pago = "NÃO";
        
        }
        try {
            servControle.alterarServico(servico.getId(),descricao, valor, pago, data, hora,tipo, funcionario);
              carregaTabela();
              desabilitaCampos();
               
        } catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } 
        
        this.altera = false;    
              
        }
        
    }//GEN-LAST:event_btSalvarActionPerformed

